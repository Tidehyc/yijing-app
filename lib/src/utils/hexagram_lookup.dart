/// 八卦对应的三爻二进制码 (自下而上, 阳=1, 阴=0)
const Map<String, String> _trigramBits = {
  '乾': '111',
  '兑': '110',
  '离': '101',
  '震': '100',
  '巽': '011',
  '坎': '010',
  '艮': '001',
  '坤': '000',
};

/// 根据上下卦名称计算六位二进制码 (自下而上)
String computeBinaryCode(String upperTrigram, String lowerTrigram) {
  final lower = _trigramBits[lowerTrigram] ?? '000';
  final upper = _trigramBits[upperTrigram] ?? '000';
  return lower + upper;
}

